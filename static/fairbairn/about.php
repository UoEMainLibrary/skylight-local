        <p>William Ronald Dodds Fairbairn (1889-1964) was a leading psychoanalyst whose pioneering work helped children suffering
        from trauma in the early 20th century. His personal library including works by Freud and Jung is held by Edinburgh
        University Library. His papers, diaries and correspondence are in the National Library of Scotland. This project,
        funded by the Wellcome Trust, has seen these collections fully catalogued and conserved and made accessible online.</p>

