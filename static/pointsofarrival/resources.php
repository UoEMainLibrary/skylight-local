            <h1 class="poa-page-title">Resource List</h4>
            <p class="page-tagline">The following websites offer resources for teachers to discuss and deepen learning with the film collection&nbsp;<em>Points of Arrival</em>.</p>

            <div class="resource-cont">
                <div id="learning"></div>
                <a href="https://www.myjewishlearning.com/" rel="noopener" target="_blank"><h2>My Jewish Learning</h2></a>
                <p>
                    This website offers short articles on all aspects of Jewish religious life and takes a broad view of Jewish history, culture and politics. 
                    The entries are written by insiders, that is by members of Jewish communities. In addition to short explanations in a widely-accessible style, 
                    there are also articles by experts, timelines, quizzes, short videos and interviews which can be useful in the classroom.&nbsp;
                    <br>
                    <a href="https://www.myjewishlearning.com/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <div id="viruallibrary"></div>
                <a href="https://www.jewishvirtuallibrary.org/" rel="noopener" target="_blank"><h2>Jewish Virtual Library</h2></a>
                <p>
                    A website offering in-depth scholarly articles about Jewish history and religion, written in accessible style. 
                    This site is complementary to <em>My Jewish Learning</em> and addressed more towards an audience of teachers adults than school students.
                    <br>
                    <a href="https://www.jewishvirtuallibrary.org/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <div id="virtualshetl"></div>
                <a href="https://sztetl.org.pl/en/" rel="noopener" target="_blank"><h2>Virtual Shetl</h2></a>
                <p>
                    Associated with Polin – Museum of the History of Polish Jews this website collects articles and images about Jewish life in Eastern Europe. 
                    Materials are written in English accessible to secondary school students, but teacher guidance is necessary to collect useful information for 
                    class discussion. The site is regularly updated and students can research the history of specific <abbr data-title="Shtetl (‘little’ town; Yiddish): Term used for a small village or 
                    market town in Eastern Europe with a large Jewish population before the Second World War.">shtetls</abbr> and their inhabitants.&nbsp;
                    <br>
                    <a href="https://sztetl.org.pl/en/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <div id="synagogues"></div>
                <a href="https://sztetl.org.pl/en/" rel="noopener" target="_blank"><h2>A visual and historical record of synagogues from around the world</h2></a>
                <p>
                    Developed in association with the Museum of the <abbr data-title="Diaspora (from Ancient Greek) Term given for the dispersion of the Israelites 
                    or Jewish people from Land of Israel and subsequent communities scattered around the world. It has come to refer to the involuntary dispersal 
                    of any people from their geographic place of origin.">Diaspora</abbr> in Tel Aviv, this website gives visual access to the history of Jewish religious buildings. 
                    The website allows viewers to ‘visit’ <abbr data-title="Synagogue: Jewish place of worship.">synagogues</abbr> in different places of the world today and brings to life historical buildings.
                    <br>
                    <a href="https://sztetl.org.pl/en/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <div id="holocaustexplained"></div>
                <a href="https://www.theholocaustexplained.org/" rel="noopener" target="_blank"><h2>The Holocaust Explained</h2></a>
                <p>
                    This website is associated with the Wiener Holocaust Library in London and provides an accessible guide to the history of the Holocaust and commonly asked questions. 
                    The site is aimed at secondary school students, and is useful also for teachers looking for reliable information for themselves and their classes.&nbsp;
                    <br>
                    <a href="https://www.theholocaustexplained.org/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <p>
                    <a href="https://wienerlibrary.co.uk/Online_Exhibitions" rel="noopener" target="_blank"><strong>The Wiener Library</strong></a> also has a number of online exhibitions which may be useful for class discussion: 
                    <br>
                    <a href="https://wienerlibrary.co.uk/Online_Exhibitions" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>   
                </p>
                <div id="holocaustencyclopedia"></div>
                <a href="https://encyclopedia.ushmm.org/" rel="noopener" target="_blank"><h2>Holocaust Encyclopedia</h2></a>
                <p>
                    This is an online resource which is produced by experts at the United States Holocaust Memorial Museum (USHMM). 
                    It is written in a style accessible to secondary school children; and it is regularly updated. 
                    The USHMM also offers classroom resources for teachers which can easily be found on their website.
                    <br>
                    <a href="https://encyclopedia.ushmm.org/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <div id="migration"></div>
                <a href="https://www.migrationmuseum.org/resource-bank/" rel="noopener" target="_blank"><h2>Migration Museum</h2></a>
                <p>
                    The UK-based charity, Migration Museum, offers virtual exhibitions and stories of refugees from all periods of British history. 
                    The link above is to their resource bank which contains materials aimed at teachers.
                    <br>
                    <a href="https://www.migrationmuseum.org/resource-bank/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <div id="artmusicculture"></div>
                <a href="https://www.benuricollection.org.uk/" rel="noopener" target="_blank"><h2>Exploring Jewish Art, Music, and Culture</h2></a>
                <p>
                    The Ben Uri Collection offers access to a wealth of Jewish art produced by 
                    British Jews from the nineteenth century onwards. The collection provides images of artworks, artist biographies, and additional information about exhibitions.
                    <br>
                    <a href="https://www.benuricollection.org.uk/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <p>
                    <a href="https://www.milkenarchive.org/" rel="noopener" target="_blank"><strong>The Milken Archive of Jewish Music</strong></a>: 1900 – Present features recordings, interviews and essays 
                    documenting the history of Jewish music in America.
                    <br>
                    <a href="https://www.milkenarchive.org/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <p>
                    The online resources of the <a href="https://thejewishmuseum.org/" rel="noopener" target="_blank"><strong>Jewish Museum</strong></a> in New York provide a window into Jewish 
                    culture and lived Jewish experience in America.
                    <br>
                    <a href="https://thejewishmuseum.org/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <div id="scotland"></div>
                <a href="https://www.scojec.org/resources/education.html" rel="noopener" target="_blank"><h2>Focus on Scotland</h2></a>
                <p>
                    The Scottish Council of Jewish Communities hosts a range of educational resources which can be freely accessed by schools. 
                    <br>
                    <a href="https://www.scojec.org/resources/education.html" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <p>
                    In addition, the exhibition<a href="https://www.scojec.org/resources/files/jewish_experience.pdf" rel="noopener" target="_blank"><strong>The Jewish Experience in Scotland</strong></a> 
                    can be viewed online.
                    <br>
                    <a href="https://www.scojec.org/resources/files/jewish_experience.pdf" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <p>
                    <a href="https://www.sjac.org.uk/schools/" rel="noopener" target="_blank"><strong>The Scottish Jewish Archives Centre</strong></a> runs educational programmes for schools, 
                    and its website offers a wealth of information for teachers. 
                    <br>
                    <a href="https://www.sjac.org.uk/schools/" rel="noopener" target="_blank"><i class="fa fa-eye"></i> Read More</a>
                </p>
                <p>
                    The archive is open to visitors and a preview of the wealth of materials held can be accessed 
