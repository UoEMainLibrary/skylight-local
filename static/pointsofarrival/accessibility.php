<h2>Technical information about this website&rsquo;s accessibility</h2>
<p>The University of Edinburgh is committed to making its websites accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018.</p>
<h2>Compliance Status</h2>
<p>This website is partially compliant with the Web Content Accessibility Guidelines 2.1 AA standard, due to the non-compliances listed below</p>
<p>The full guidelines are available at</p>
<p>&nbsp;<a href="https://www.w3.org/TR/WCAG21/">Web Content Accessibility Guidelines version 2.1</a></p>
<h3>Non accessible content</h3>
<p>The content listed below is non-accessible for the following reasons.</p>
<p>The following items to not comply with the WCAG 2.1 AA success criteria</p>
<ul>
    <li>Some non-text content does not have text alternatives
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG21/#non-text-content">1.1.1 - Non-text Content</a></li>
        </ul>
    </li>
    <li>Not all page content is compatible with assistive technologies.
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG21/#info-and-relationships">1.3.1 -&nbsp;Info and Relationships</a></li>
            <li><a href="https://www.w3.org/TR/WCAG21/#name-role-value">4.1.2&nbsp;- Name, Role Value</a></li>
        </ul>
    </li>
    <li>Some colour contrasts do not meet recommended guidance.
        <ul>
            <li><u><a href="https://www.w3.org/TR/WCAG21/#contrast-minimum">1.4.3 -&nbsp;Contrast (Minimum)</a></u></li>
        </ul>
    </li>
    <li>Visual information to identify user interface components, such as keyboard focus, do not always have a sufficient contrast ratio
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG21/#non-text-contrast">1.4.11 - Non-text contrast</a></li>
        </ul>
    </li>
    <li>Most tooltips disappear as soon as the cursor moves. Also tooltips are not always present for all icons and images
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG21/#content-on-hover-or-focus">1.4.13 - Content on Hover or Focus</a></li>
        </ul>
    </li>
    <li>It is not always clear to tell where you have navigated to when you are using a keyboard
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG21/#focus-visible">2.4.7 - Focus Visible</a></li>
        </ul>
    </li>
    <li>There is no specific help function section, including accessibility information
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG21/#help">3.3.5 - Help</a></li>
        </ul>
    </li>
</ul>
<p>A complete solution or significant improvement will be in place by March 2021 where the issues are within our control.</p>
<h3>Disproportionate burden</h3>
<p>We are not currently claiming that any&nbsp;accessibility problems would be a disproportionate burden to fix.&nbsp;</p>
<h3>Content that is not within the Scope of the Accessibility Regulations</h3>
<h3>Reproduction of Heritage Collections</h3>
<p>The nature of the website means there are numerous representations of <a href="https://collections.ed.ac.uk/stcecilias/search">the musical instrument heritage collections</a> contained within. There is technical information about the images of these items, including titles and description. However, reproducing items within heritage collections would be <a href="https://www.legislation.gov.uk/uksi/2018/952/regulation/4/made">exempt from meeting the accessibility regulations</a>, but all effort has been made to make the reproduction of these heritage collections as accessible as possible.</p>
<h2>What we're doing to improve accessibility</h2>
<p>We will continue to work with our in house developers to address these issues and deliver a solution or suitable workaround and correct issues directly where they are under our control.</p>
<p>We will continue to monitor accessibility and will carry out further accessibility testing if significant changes are made to the user interface or if a service user raises an issue, and plan to resolve the issues that are within our control by March 2021. We plan to manual review the accessibility of the site and make improvements and update this statement before December 2021.Where we are unable to resolve an issue or where an issue is out with our control we will ensure reasonable adjustments are put in place to ensure no user is disadvantaged.</p>
