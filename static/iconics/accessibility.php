<h2>How accessible this website is</h2>
<p>

</p>
<p>We know some parts of this website are not fully accessible:</p>
<p>

</p>

<!-- {{This section varies according to the site. Edit as appropriate}} -->

<ul>
<li>There are some areas where the colour contrast does not meet the recommend standards.</li>
<li>There is content distortion when zooming up to 200%.</li>
<li>Reflow is not enabled to 400%.</li>
<li>It is not always clear where a user has navigated to when using keyboard only.</li>
<li>Some non-text content does not have alternative text.</li>
<li>Some information is conveyed only in colour e.g. links.</li>
<li>Some touch targets have insufficient size and space.</li>
<li>Text appear as an image.</li>
<li>Some texts are displayed over a patterned background.</li>
<li>All links do not necessarily have meaningful hypertext.</li>
<li>Some hyperlinks are conveyed in colour only and are not underlined.</li>
<li>Some hyperlinks are plain URLs.</li>
<li>Some titles appear on the tiles suddenly on hover over or keyboard focus and then disappear.</li>
<li>Some links open in a new tab/window without warning.</li>
<li>Some part of the keyboard access does not follow a meaningful sequence.</li>
<li>There's no skip to main content option.</li>
<li>Assistive software such as voice recognition and screen readers are not fully compatible with the website.</li>
</ul>
