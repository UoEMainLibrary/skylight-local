        <p>This collection is compiled from readers’ orders for scans, principally in black and white and some in colour. They highlight the range of resources at The University of Edinburgh’s Centre for Research Collections, from Herschel’s music manuscripts, to ‘An Essay on the Character and Cerebral Development of Robert Burns,’ to Charles Lyell’s handwritten notes.
            The images that have been created by the University's Digital Imaging Unit are freely accessible to all. The PDFs are supplied under a Creative Commons CC-BY License: you may share and adapt for any purpose as long as attribution is given to the University of Edinburgh. Further information is available at <a href="http://creativecommons.org/licenses/by/4.0/" target="_blank">http://creativecommons.org/licenses/by/4.0/ (opens in a new tab)</a></p>

        <h1>University Collections</h1>
        <p>
            Special Collections include manuscripts and archives, rare books, photographs, printed and handwritten music, architectural drawings, theses and microfilms. The collections relate to University of Edinburgh and its history, and to Scotland and the wider world.</p>
        <p>In total we have about 35 kilometres of historic material, much of which cannot be found anywhere else in the world. <a href="http://collections.ed.ac.uk" Title="University Collections Home">collections.ed.ac.uk</a>
