        <h2 style="font-size:20pt !important;"><span style="font-family:Arial">How accessible this website is</span></h2>
        <p><span style="font-family:Arial; -aw-import:ignore">&#xa0;</span></p>
        <p><span style="font-family:Arial">We know some parts of this website are not fully accessible:</span></p>
        <p><span style="font-family:Arial; -aw-import:ignore">&#xa0;</span></p>
        <ul type="disc" style="margin:0pt; padding-left:0pt">
            <li class="ListParagraph"
                style="margin-left:35.27pt; line-height:107%; padding-left:7.48pt; font-family:serif; -aw-font-family:'Symbol'; -aw-font-weight:normal; -aw-number-format:''">
                <span style="font-family:Arial">there are images of text</span></li>
            <li class="ListParagraph"
                style="margin-left:35.27pt; line-height:107%; padding-left:7.48pt; font-family:serif; -aw-font-family:'Symbol'; -aw-font-weight:normal; -aw-number-format:''">
                <span style="font-family:Arial">all images do not have alt text </span></li>
            <li class="ListParagraph"
                style="margin-left:35.27pt; line-height:107%; padding-left:7.48pt; font-family:serif; -aw-font-family:'Symbol'; -aw-font-weight:normal; -aw-number-format:''">
                <span style="font-family:Arial">some hyperlinks are conveyed with colour only</span></li>
            <li class="ListParagraph"
                style="margin-left:35.27pt; line-height:107%; padding-left:7.48pt; font-family:serif; -aw-font-family:'Symbol'; -aw-font-weight:normal; -aw-number-format:''">
                <span style="font-family:Arial">some hyperlinks are not formatted correctly as meaningful
                    hypertext</span></li>
            <li class="ListParagraph"
                style="margin-left:35.27pt; line-height:107%; padding-left:7.48pt; font-family:serif; -aw-font-family:'Symbol'; -aw-font-weight:normal; -aw-number-format:''">
                <span style="font-family:Arial">not all non-text content presented to users has alternative text</span>
            </li>
            <li class="ListParagraph"
                style="margin-left:35.27pt; line-height:107%; padding-left:7.48pt; font-family:serif; -aw-font-family:'Symbol'; -aw-font-weight:normal; -aw-number-format:''">
                <span style="font-family:Arial">it is not possible to access all content by using the keyboard
                    alone</span></li>
            <li class="ListParagraph"
                style="margin-left:35.27pt; line-height:107%; padding-left:7.48pt; font-family:serif; -aw-font-family:'Symbol'; -aw-font-weight:normal; -aw-number-format:''">
                <span style="font-family:Arial">keyboard navigation does not follow logical ordering</span></li>
            <li class="ListParagraph"
                style="margin-left:35.27pt; line-height:107%; padding-left:7.48pt; font-family:serif; -aw-font-family:'Symbol'; -aw-font-weight:normal; -aw-number-format:''">
                <span style="font-family:Arial">not all icons have tooltips and these are available via mouse hover but
                    not when using the keyboard or assistive software </span></li>
            <li class="ListParagraph"
