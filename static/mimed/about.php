        <h1>Musical Instrument Museums at Edinburgh</h1>
        <p>
           Edinburgh University Collection of Historic Musical Instruments ranks among the world's most important collections of musical heritage. All the main types or musical sound-making device are represented, and are presented to the public in two museums. The c. 5000 objects in the permanent collection constitute a rich research resource of both breadth and depth, and include many historically typical models of musical instrument spanning over 500 years together with prized rare and unique items. The instruments are supplemented by an archive of original materials, working papers and a sound archive. The Collection as a whole attracts researchers from far and wide and is an extensively cited resource in international scholarship. Instruments are lent from time to time to prestigious exhibitions at home and overseas.</p>
        <p>Edinburgh University Collection of Historic Musical Instruments is recognised by the Scottish Government as a Significant Collection.
        </p>
        <h1>University Collections</h1>
        <p>
            Special Collections include manuscripts and archives, rare books, photographs, printed and handwritten music,
            architectural drawings, theses and microfilms. The collections relate to University of Edinburgh and its history,
            and to Scotland and the wider world.
        </p>
        <p>
            In total we have about 35 kilometres of historic material, much of which cannot be found anywhere else in the world.
        </p>
        <h1>Musical Instrument Museums at Edinburgh Online</h1>
        <p>
            We aim to represent all of the University's rare and unique instruments online through this resource.
            This is an ongoing process as the existing collection continues to be catalogued and photographed and also as new acquisitions are brought in.
         </p>
