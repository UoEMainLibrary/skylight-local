      <h1>How to participate</h1>
      <p>
          Everyone remembers the Library – the conversations in the café, the rush for books as exams and essay deadlines
          loomed, the moment of discovery when you found a resource that answered your questions. The Library is and always
          will be the beating heart of the University.
      </p>
      <p> Last year the Library received a record two million visits. Whilst this is wonderful, it also takes a toll on
          our resources. Students and alumni come not only to use the books but to view online journals, e-books, museum
          objects and works of art. With over 35km of Special Collections and increased interest in them, they need extra
          support to help cope with this growing and changing demand.
      </p>
        <h3>Volunteer with us</h3>
        <p>There are a range of ways to get involved with our collections, and we have a number of volunteer opportunities
            to suit all abilities and areas of interest.  If you would like to volunteer with us, please go to:
            <a href="https://www.ed.ac.uk/visit/museums-galleries/heritage-collections/volunteers-interns" target="_blank">volunteers and interns (opens in a new tab)</a> for more information.
        </p>
        <h3>Library Foundation Fund</h3>
      <p>The <a href="http://www.ed.ac.uk/schools-departments/information-services/about/organisation/library-and-collections/library-foundation-fund" title="Library Foundation Fund">Library Foundation Fund</a>
          has been set up to help us build on 400 years of collecting and to seize and harness the technological opportunities
          of the 21st century. With your help we will be able to:
          <ul>
          <li>preserve and conserve the most unique, fragile and precious</li>
            <li>documents in the Collection</li>
            <li>digitise books and manuscripts to make them available to you and the worldwide community</li>
            <li>support curators, students and researchers to uncover hidden gems</li>
            <li>purchase books and archives which belong in Edinburgh and will enhance our heritage collection</li>
            <li>promote the collections through exhibitions and events</li>
        </ul>
           If you would like to find out more about the Library Foundation Fund or chat to our Development Officer
            about philanthropic gifts, bequests or legacies then please call Leisa Thomas on 0131 650 2789.
       </p>
