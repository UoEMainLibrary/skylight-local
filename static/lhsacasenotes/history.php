        <h1 class="itemtitle">Case histories: Norman Dott's neurosurgical case notes</h1>

        <h1 class="itemtitle">Neurosurgery at the Royal Infirmary of Edinburgh</h1>
        <img src="<?php echo base_url()?>/theme/lhsacasenotes/images/P-PL1-S-103d.gif" alt="Royal Infirmary of Edinburgh" class="img-responsive pull-right img-circle" />

        <p>When Dott started his career in the 1920s there were no dedicated facilities for
            neurosurgery or neurorehabilitation. Dott worked in private practice at first, using his own consulting rooms and
            beds in a private nursing home in Edinburgh’s Great King Street, moving his surgical equipment in a taxi.</p>
        <p>In 1931, Dott was appointed Associate Neurological Surgeon at the Royal Infirmary of Edinburgh (RIE) and was
            given access to four post-operative beds for his patients in Wards 13 and 14 and use of the operating
            theatre by surgeon, Sir David Wilkie. This was the beginning of the Department of Surgical Neurology at the
            RIE, which was the first of its kind in Scotland. This move was followed in 1936 with the allocation of Ward
            20, located in the Infirmary clock tower, for Dott to set up his own department. However it wasn’t until 1938
            that Ward 20 received its first patients, as space had to be extended and made fit for purpose. Changes
            included adding an operating theatre (with a special steel elliptical lighting dome imported from Paris
            and a sound-proof viewing room which featured one-way glass for students to view operations), twenty beds,
            an ophthalmic room, staff-room and out-patient facilities.The modifications to the department were aided by
            a grant from the Rockefeller Foundation, a contribution from Dott and his circle, various individual
            donations and funds from the trust of Sir Alexander Grant, head of biscuit manufacturer McVitie and Price Ltd.</p>

        <p>During and after the Second World War, Ward 20 worked alongside the Brain Injuries Unit at Bangour General
            Hospital. On the opening of the new Department of Surgical Neurology in the Western General Hospital, Ward 20
            became a specialist head injuries’ unit dealing with acute and out-patient care.</p>

        <h1 class="itemtitle">Bangour General Hospital</h1>
        <img src="<?php echo base_url()?>/theme/lhsacasenotes/images/P-PL44-B-E-009d.gif" alt="Bangour General Hospital" class="img-responsive pull-right img-circle" />

        <p>During the Second World War, Norman Dott set up the Brain Injuries Unit inside Bangour General Emergency Medical
            Service Hospital in Broxburn, West Lothian.</p>
        <p>As an Emergency Medical Service Hospital, Bangour General was built as in 1939 as additional accommodation for
            wartime casualties. It was an annexe to Bangour Village Hospital, originally a psychiatric institution (or
            ‘asylum’) which became Edinburgh War Hospital during the conflict. When the expected influx of civilian
            casualties did not occur, Dott was given space for a specialist unit.  Dott had scope for two neurosurgical
            teams in Bangour, the second under Mr G.L Alexander. Other neurology specialists working at Bangour included:
            Dr Kate Hermann, a German Jewish neurologist who had fled from Nazi Germany before the outbreak of the Second
            World War; psychiatrist D.K Henderson; anaesthetist G Maxwell Brown; junior anaesthetist Elizabeth Batters;
            psychologist O.L Zangwill; physicist C.A Beevors; and neuropathologist W Blackwood. The unit treated patients
            from the forces and auxiliaries, along with military pensioners and prisoners of war.</p>
        <p>In 1941, Dott was made Consultant in Neurosurgery to the Army in Scotland and Consultant Neurosurgeon to the
            Emergency Medical Service. He was also a member of the National Brain Injuries Committee throughout the war,
            and was awarded the Commander of the Order of the British Empire (CBE) for his work in Bangour in 1948.
            After the war, the Brain Injuries Unit at Bangour continued to treat patients in tandem with Dott’s team in
            Ward 20 of the RIE until a new Department of Surgical Neurology opened at the Western General Hospital in 1960.</p>

        <h1 class="itemtitle">Department of Surgical Neurology, Western General Hospital</h1>
        <img src="<?php echo base_url()?>/theme/lhsacasenotes/images/LHB11-7-2d.gif" alt="Western General Hospital" class="img-responsive pull-right img-circle" />

        <p>Dott took charge of a new Department of Surgical Neurology at the Western General Hospital, Edinburgh
            (WGH). This new department brought together the facilities for the diagnosis, treatment and rehabilitation
            of surgical neurology patients that Dott had been working towards throughout his career.</p>
        <p>The Department of Surgical Neurology in Ward 20 of the RIE was firmly established as
            a specialist unit by the late 1950s. It operated over two sites providing different services at each location
            throughout the 1940s and 1950s. Although the department was extended in the 1950s, the growing number of
            patients and the demands on the expertise of Dott and his team meant that more space
            was needed. A major contributing factor to the growth in patient numbers was the increase in head injuries
            sustained in road accidents due to the rise in car use.</p>
        <p>A six-storey block was built at the WGH at a cost of £500,000. The new building housed twin operating theatres;
            sixty beds; physiotherapy, hydrotherapy
            and occupational therapy rooms; and staff accommodation.The theatres, which were specially designed by Dott
            and described by fellow surgeons as ‘utopian’, were an ovoid shape designed to limit infection and had domed
            roofs which featured shadowless lamps.</p>
        <p>The new department at WGH was mainly for elective surgery, with patients transferring to and from the RIE.
            The sites were even more equipped to work together when, in 1962 a television link was set up. Norman
            Dott retired in 1963, but the department that he established continued to grow and lead the way in research
            and treatment, finally merging in 1986 with the Department of Medical Neurology to form the Department of
            Clinical Neurosciences. In 2018, the Department of Clinical Neurosciences will move to Little France, with
            neurosurgery services returning to the RIE.</p>
